package com.sec01.exam;

//정수 100과 200을 a,b라는 이름으로 저장 후 출력 하고 싶다.
public class a_exam {
		//전역변수 영역
	public static void main(String[] args) {
		//지역변수 영역;
		//int a = 100;
		int a = 0; //변수 a 정수형으로 선언 후 0으로 초기화
		a = 100; //a에 숫자 100 대입
		System.out.println(a);
		
		
		int b = 200;//초기화 하면서 값 대입
		System.out.println(b);

	}

}
